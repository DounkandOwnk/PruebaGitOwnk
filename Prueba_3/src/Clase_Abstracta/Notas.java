package Clase_Abstracta;

public abstract class Notas {

	private double nota;
	private String pasoAño;
	
	public double getNota() {
		return nota;
	}
	public void setNota(double nota) {
		this.nota = nota;
	}
	public String getPasoAño() {
		return pasoAño;
	}
	public void setPasoAño(String pasoAño) {
		this.pasoAño = pasoAño;
	}
	
}
